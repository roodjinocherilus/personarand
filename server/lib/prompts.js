function buildUserMessage({ type, platform, topic, tone, length, funnel_layer, extra }) {
  const parts = [];
  parts.push(`FORMAT: ${type}`);
  if (platform) parts.push(`PLATFORM: ${platform}`);
  if (funnel_layer) parts.push(`FUNNEL LAYER: ${funnel_layer}`);
  if (tone && TONE_DESCRIPTORS[tone]) parts.push(`TONE: ${TONE_DESCRIPTORS[tone]}`);
  if (length && LENGTH_MODIFIERS[length]) parts.push(`LENGTH: ${LENGTH_MODIFIERS[length]}`);
  parts.push('');
  parts.push('TOPIC / BRIEF:');
  parts.push(topic || '(no specific topic \u2014 use the funnel layer and platform to choose a strong angle from the key themes)');
  parts.push('');
  parts.push('FORMAT INSTRUCTIONS:');
  parts.push(FORMAT_INSTRUCTIONS[type] || FORMAT_INSTRUCTIONS['linkedin-short']);
  if (extra) {
    parts.push('');
    parts.push('ADDITIONAL DIRECTION:');
    parts.push(extra);
  }
  return parts.join('\n');
}

/**
 * Build the brand voice system prompt. If a structured voice profile is
 * provided AND it compiles to a non-null string (passes the minimum-content
 * gate in voiceProfile.js), use that. Otherwise fall back to the legacy
 * Roodjino-shaped BRAND_SYSTEM_PROMPT so cold-start installs still produce
 * sensible output.
 *
 * Lazy-required to avoid circular imports (voiceProfile.js doesn't import
 * prompts.js today, but keeping the dependency one-way protects future edits).
 */
function buildBrandSystemPrompt(profile) {
  if (!profile) return BRAND_SYSTEM_PROMPT;
  const { compileProfileToSystemPrompt } = require('./voiceProfile');
  const compiled = compileProfileToSystemPrompt(profile);
  return compiled || BRAND_SYSTEM_PROMPT;
}

